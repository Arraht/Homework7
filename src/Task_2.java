public class Task_2 {
    public static void main(String[] args) {
        int number = 0;
        while (number < 10) {
            number++;
            System.out.print(number + " ");
        }
        System.out.println();
        for (int i = number; i > 0 ; i--) {
            System.out.print(i + " ");
        }
    }
}
